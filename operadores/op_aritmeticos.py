#suma y resta (+ y -)
suma = 5 + 3
resta = 5 - 3

#multiplicacion y division (* y /)
multiplicacion = 5 * 3
division = 5 / 3

#division entera y modulo (// y %)
division_entera = 5 // 3
modulo = 5 % 3

#potencia (**)
potencia = 5 ** 3

#Imprimir resultados
print("Suma:", suma)
print("Resta:", resta)
print("Multiplicacion:", multiplicacion)
print("Division:", division)
print("Division entera:", division_entera)
print("Modulo:", modulo)
print("Potencia:", potencia)
